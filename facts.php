                    <li><strong>Social bullying</strong>, <a id="social"></a>sometimes referred to as relational bullying, involves hurting someone’s reputation or relationships. Social bullying includes:
                <ul><li>Leaving someone out on purpose</li>
                    <li>Telling other children not to be friends with someone</li>
                    <li>Spreading rumors about someone</li>
                    <li>Embarrassing someone in public</li>
                </ul></li>
        <br>

                    <li><strong>Physical bullying </strong>involves hurting a person’s body or possessions. Physical bullying includes:
                <ul><li>Hitting/kicking/pinching</li>
