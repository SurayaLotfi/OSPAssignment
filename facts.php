            <strong>Effects of Bullying</strong>
        </p>
        <section>
        <div class = "container-center">

            <div class="section-content">
                <ul><li><strong>Kids who are bullied</strong> may experience negative physical, social, emotional, academic, and mental health issues. For example:
                <ul><li>Depression</li>
                    <li>Anxiety</li>
                    <li>Low self-esteem</li>
                    <li>Decreased academic achievement</li>
                    <li>Victims of bullying may experience difficulty trusting others and may struggle with forming healthy relationships
                </ul></li>
        <br>
    
                    <li><strong>Kids who bully others</strong> <a id="social"></a> tend to engage in violent and other risky behaviors into adulthood. For example:
                <ul><li>Abuse alcohol and other drugs in adolescence and as adults</li>
                    <li>Get into fights, vandalize property, and drop out of school</li>
                    <li>Be abusive toward their romantic partners, spouses, or children as adults</li>
                    <li>Have criminal convictions and traffic citations as adults </li>
                </ul></li>
        <br>

                    <li><strong>Bystanders </strong> more likely to:
                <ul><li>Have increased use of tobacco, alcohol, or other drugs</li>
